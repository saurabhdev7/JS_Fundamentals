let number = 10.576;

console.log(Math.round(number));

console.log(Math.floor(number));        // Math.floor is used to round down the number to the nearest integer value less than the number.

console.log(Math.ceil(number));        // Math.ceil is used to round up the number to the nearest integer value greater than the number.

console.log(Math.trunc(number));        // Math.trunc is used to remove the decimal part of the number.

let random = Math.random();        // Math.random is used to generate a random number between 0 and 1.

console.log(Math.round(random * 100)); 